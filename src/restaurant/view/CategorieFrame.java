    private void initCategoryList() {

    // catégories par défaut
    categoryModel.addElement("Legumes");
    categoryModel.addElement("Boissons");
    categoryModel.addElement("Fruits");
    categoryModel.addElement("Entrees");
    categoryModel.addElement("Desserts");

    jList1.setModel(categoryModel);
    jList1.setFixedCellHeight(40);
    jList1.addListSelectionListener(e -> {
    if (!e.getValueIsAdjusting()) {
        String selected = jList1.getSelectedValue();
        jTextField1.setText(selected);
    }
});

}
